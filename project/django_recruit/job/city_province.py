def city_to_province_map_name(city: str) -> str | None:
    """将城市简称/名称转为地图省级名称；无法识别时返回 None。"""
    if not city:
        return None
    name = city.strip()
    if not name:
        return None
    if name in _CITY_TO_PROVINCE:
        return _CITY_TO_PROVINCE[name]
    # 常见后缀
    for suf in ("市", "县", "区", "州", "盟", "地区"):
        if name.endswith(suf) and len(name) > len(suf):
            core = name[: -len(suf)]
            if core in _CITY_TO_PROVINCE:
                return _CITY_TO_PROVINCE[core]
    # 自治区 / 省关键字（数据源偶发带全称）
    if "内蒙古" in name or name.startswith("内蒙古"):
        return "内蒙古自治区"
    if "广西" in name:
        return "广西壮族自治区"
    if "西藏" in name:
        return "西藏自治区"
    if "宁夏" in name:
        return "宁夏回族自治区"
    if "新疆" in name:
        return "新疆维吾尔自治区"
    if name.endswith("省"):
        base = name[:-1]
        prov_from_suffix = {
            "河北": "河北省",
            "山西": "山西省",
            "辽宁": "辽宁省",
            "吉林": "吉林省",
            "黑龙江": "黑龙江省",
            "江苏": "江苏省",
            "浙江": "浙江省",
            "安徽": "安徽省",
            "福建": "福建省",
            "江西": "江西省",
            "山东": "山东省",
            "河南": "河南省",
            "湖北": "湖北省",
            "湖南": "湖南省",
            "广东": "广东省",
            "海南": "海南省",
            "四川": "四川省",
            "贵州": "贵州省",
            "云南": "云南省",
            "陕西": "陕西省",
            "甘肃": "甘肃省",
            "青海": "青海省",
        }
        return prov_from_suffix.get(base)
    return None
