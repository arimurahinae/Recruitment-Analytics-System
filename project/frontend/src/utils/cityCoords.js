export function cityToScatterData(byCityList) {
  if (!Array.isArray(byCityList)) return [];
  return byCityList
    .map((item) => {
      const name = item.name || "";
      const coord = CITY_COORDS[name];
      if (!coord) return null;
      return { name, value: [...coord, item.value || 0] };
    })
    .filter(Boolean);
}
